abstract class Base {
    abstract public function __construct($options);

    abstract public function saveFile ($url, $target_location = null);

    abstract public function getFile ($url, $options = []);

    /**
     * @param $url
     * @param $file \Floxim\FileStorage\File
     * @param array $meta
     * @param array $options
     * @return mixed
     */
    abstract public function putFile ($url, $file, $meta = [], $options = []);

    abstract public function putFileBody ($url, $body, $meta = []);

    public static function getContentTypeByUrl($url)
    {
        if (!preg_match("~\.([a-z0-9]+)$~", $url, $ext)) {
            return;
        }
        $ext = $ext[1];
        if (isset(self::$mime_table[$ext])) {
            return self::$mime_table[$ext];
        }
    }

    /**
     * @param $body
     * @param $meta
     * @return \Floxim\FileStorage\File
     */
    public function createFileFromBody ($body, $meta = [])
    {
        return File::createFromBody($body, $meta);
    }

